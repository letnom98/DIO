
public class Client {
	private String name;
	private int age;
	private int socialSecurityNumber;
	
	public Client(String name, int age, int SocialSecurityNumber) {
		this.name = name;
		this.age = age;
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSocialSecurityNumber() {
		return socialSecurityNumber;
	}
	public int getAge() {
		return age;
	}
}
